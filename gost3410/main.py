from gost import Gost
from ec import ECPoint
from point import Point


def test_sign():
    P = Point(
        2,
        4018974056539037503335449422937059775635739389905545080690979365213431566280)

    p = 57896044618658097711785492504343953926634992332820282019728792003956564821041
    a = 7
    b = 43308876546767276905765904595650931995942111794451039583252968842033849580414
    q = 57896044618658097711785492504343953927082934583725450622380973592137631069619

    gost = Gost(p, a, b, q, P.x, P.y)
    key = 55441196065363246126355624130324183196576709222340016572108097750006097525544
    message = 20798893674476452017134061561508270130637142515379653289952617252661468872421
    k = 53854137677348463731403841147996619241504003434302020712960838528893196233395

    sign = gost.sign(message, key, k)

    expected = (29700980915817952874371204983938256990422752107994319651632687982059210933395,
                574973400270084654178925310019147038455227042649098563933718999175515839552)

    assert sign == expected


def test_verify():
    P = Point(
        2, 4018974056539037503335449422937059775635739389905545080690979365213431566280)
    Q = Point(57520216126176808443631405023338071176630104906313632182896741342206604859403,
              17614944419213781543809391949654080031942662045363639260709847859438286763994)

    print('Координаты точки P:')
    print('x: {}\ny: {}'.format(P.x, P.y))

    print('Координаты точки Q:')
    print('x: {}\ny: {}\n'.format(Q.x, Q.y))

    p = 57896044618658097711785492504343953926634992332820282019728792003956564821041
    a = 7
    b = 43308876546767276905765904595650931995942111794451039583252968842033849580414
    q = 57896044618658097711785492504343953927082934583725450622380973592137631069619

    print('p:', p)
    print('a:', a)
    print('b:', b)
    print('q:', q)

    gost = Gost(p, a, b, q, P.x, P.y)
    
    key = 55441196065363246126355624130324183196576709222340016572108097750006097525544
    message = 32423423434
    k = 53854137677348463731403841147996619241504003434302020712960838528893196233395

    sign = gost.sign(message, key, k)
    public_key = ECPoint(Q.x, Q.y, a, b, p)

    print('Закодированное в целое число сообщение:', message)
    print('Полученная подпись:', sign)
    print('Проверена ли подпись?', gost.verify(message, sign, public_key))

    
    assert gost.verify(message, sign, public_key) == True


def main():
    test_sign()
    test_verify()


if __name__ == "__main__":
    main()
